package loop;

public class ForDemo {
    public static void main(String[] args) {
        for (int i = 0; i < 10; i++) {
            System.out.println("coding" + i);
        }
        System.out.println("=================");
        for (int j = 10; j > 1; j=j-2) {
            System.out.println("coding" + j);
        }
    }
}
